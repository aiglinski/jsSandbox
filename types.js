// PRIMATIVE TYPES
// string
const name = 'John Doe';
console.log(typeof name);
// number
const age = 45;
console.log(typeof age);
// Boolean
const haskids = true;
console.log(typeof haskids);
// null
const hascar = null;
console.log(typeof hascar);
// undefined
let test;
console.log(typeof test);
// symbol
const sym = Symbol();
console.log(typeof sym);

// REFERENCE TYPES

// array
const hobbies = ['biking', 'reading', 'coding'];
console.log(typeof hobbies);
// object literal
const address = {
  city: 'Milwaukee',
  state: 'Wisconsin',
  location: 'East Side'
};
console.log(typeof address);
// Date
const today = new Date();
console.log(today);
console.log(typeof today);
